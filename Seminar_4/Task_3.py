data = ["один", "два","три","четыре", "пять", "шесть", "семь", ]
a, b, c, *d = data # переменная с * сохранит в себя лишние значения
print (f'{a=} {b=} {c=} {d=}')

a, b, *c, d = data
print(f'{a=} {b=} {c=} {d=}')

a, *b, c, d = data
print(f'{a=} {b=} {c=} {d=}')

*a, b, c, d = data
print (f'{a=} {b=} {c=} {d=}')